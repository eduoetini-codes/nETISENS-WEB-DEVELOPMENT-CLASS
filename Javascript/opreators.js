// Arithemtic Operators
// + ( Addition) : a + b
let num1 = 5;
let num2 = 9;

console.log(num1 + num2);

// - (subtraction) : a - b
let num3 = 71;
let num4 = 14;

console.log(num3 - num4 - num2);

// * (Multiplication): a * b
console.log(num3 * num1);

// / (Division): a / b
console.log(num3 / num1);

// % (Modulus); a % b 
console.log(num4 % num1);

// ++ (Increment): a++ or ++a
let a = 6;
console.log({a});
a++;
console.log({a});

// -- (Decrement); --a or b--
let b;
console.log({b})
b--
console.log({b})